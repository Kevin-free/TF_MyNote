package com.kevin.jpa.entity;

public class Person {
    private Integer num;
    private String name;

    public Person() {
    }

    public Integer getNum() {
        return num;
    }

    public void setNum(Integer num) {
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
